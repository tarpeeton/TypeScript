//  =====  String type
let myName:string = ''
// Type 'number' is not assignable to type 'string'.
// myName = 1
// Faqatgina String qabul qiladi
myName = "Rustam"

// ===== Number type

let myAge:number = 0
// Type 'string' is not assignable to type 'number'.
// myAge = "Rustam"
// 17 faqatgina number qabul qiladi
myAge = 17

// ====== Boolean type
let isMarried:boolean = false
//Type 'string' is not assignable to type 'boolean'.
// isMarried = "Rustam"

//  Faqatgina Boolean qabul yani True false
isMarried = true

// ======= Undifined type

let undifined: undefined = undefined;
// Type 'true' is not assignable to type 'undefined'
// undifined = true

//  Faqatgina Undifined Qabul qiladi
undifined = undefined

//  ====== Null type

let myNumber: null = null
// Type 'true' is not assignable to type 'null'.
// myNumber = true

//  Faqatgina Null Qabul qiladi
myNumber = null